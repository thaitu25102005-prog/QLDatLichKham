-- SP thêm bệnh nhân mới
CREATE PROCEDURE PROC_ThemBenhNhan
    @HOTENBENHNHAN NVARCHAR(100),
    @CCCD VARCHAR(12),
    @SDT VARCHAR(15),
    @GIOITINH NVARCHAR(10),
    @BACSI_ID INT
AS
BEGIN
    INSERT INTO BENHNHAN(HOTENBENHNHAN, CCCD, SDT, GIOITINH, BACSI_ID)
    VALUES (@HOTENBENHNHAN, @CCCD, @SDT, @GIOITINH, @BACSI_ID)
END
GO

-- SP thống kê dịch vụ sử dụng nhiều nhất
CREATE PROCEDURE SP_DICHVUSUDUNGNHIEU
AS
BEGIN
    SELECT
        DV.DV_ID        AS MADV,
        DV.TENDV        AS TENDV,
        COUNT(CT.CTHD_ID)          AS SOLANSUDUNG,
        SUM(ISNULL(CT.SOLUONG, 0)) AS TONGSOLUONG
    FROM DICHVU DV
    LEFT JOIN CHITIETHOADON CT ON CT.DICHVU_ID = DV.DV_ID
    GROUP BY DV.DV_ID, DV.TENDV
    ORDER BY SOLANSUDUNG DESC
END
GO

-- ==========================================
-- FUNCTIONS
-- ==========================================

-- Tìm bác sĩ theo tên
CREATE FUNCTION TimBacSiTheoTen(@TEN NVARCHAR(100))
RETURNS TABLE
AS
RETURN (
    SELECT * FROM BACSI WHERE TENBACSI LIKE N'%' + @TEN + '%'
)
GO

-- Tìm kiếm khoa theo tên
CREATE FUNCTION FN_TimKiemKhoa(@TEN NVARCHAR(100))
RETURNS TABLE
AS
RETURN (
    SELECT * FROM KHOA WHERE TENKHOA LIKE N'%' + @TEN + '%'
)
GO

-- Xuất kết quả khám theo bệnh nhân (tra cứu bằng CCCD hoặc mã BN)
CREATE FUNCTION FN_XuatKetQuaDaKham_TheoBN(@MABN VARCHAR(5), @CCCD VARCHAR(12))
RETURNS TABLE
AS
RETURN (
    SELECT
        KQ.MAKQ,
        BN.MABENHNHAN,
        BN.HOTENBENHNHAN,
        BN.GIOITINH,
        BN.SDT,
        BS.MABACSI,
        BS.TENBACSI,
        K.TENKHOA,
        DL.NGAYKHAM,
        CK.GIOKHAM,
        KQ.CHUANDOAN,
        KQ.KETLUAN,
        KQ.THANHVIEN,
        KQ.GHICHU
    FROM KETQUAKHAM KQ
    JOIN DATLICHKHAM DL ON KQ.DATLICHKHAM_ID = DL.DATLICHKHAM_ID
    JOIN BENHNHAN BN     ON DL.BNHAN_ID       = BN.BNHAN_ID
    JOIN BACSI BS        ON DL.BACSI_ID        = BS.BACSI_ID
    LEFT JOIN KHOA K            ON BS.KHOA_ID   = K.KHOA_ID
    LEFT JOIN CAKHAM_MAPPING CK ON DL.CAKHAM_ID = CK.CAKHAM_ID
    WHERE (@MABN IS NULL OR BN.MABENHNHAN = @MABN)
      AND (@CCCD IS NULL OR BN.CCCD = @CCCD)
)
GO


-- ==========================================
-- TẠO BẢNG
-- ==========================================

-- 1. Table BENHVIEN
CREATE TABLE BENHVIEN (
    BENHVIEN_ID INT IDENTITY(1,1) PRIMARY KEY,
    MABENHVIEN AS ('BV' + RIGHT('00' + CAST(BENHVIEN_ID AS VARCHAR(3)), 2)) PERSISTED,
    TENBENHVIEN NVARCHAR(100),
    SDT VARCHAR(15),
    DIACHI NVARCHAR(200)
);

-- 2. Table KHOA
CREATE TABLE KHOA (
    KHOA_ID INT IDENTITY(1,1) PRIMARY KEY,
    MAKHOA AS ('K' + RIGHT('000' + CAST(KHOA_ID AS VARCHAR(4)), 3)) PERSISTED,
    TENKHOA NVARCHAR(100),
    BENHVIEN_ID INT,
    SOBS INT,
    CONSTRAINT FK_KHOA_BENHVIEN FOREIGN KEY (BENHVIEN_ID) REFERENCES BENHVIEN(BENHVIEN_ID) ON DELETE CASCADE
);

-- 3. Table BACSI
CREATE TABLE BACSI (
    BACSI_ID INT IDENTITY(1,1) PRIMARY KEY,
    MABACSI AS ('BS' + RIGHT('000' + CAST(BACSI_ID AS VARCHAR(4)), 3)) PERSISTED,
    TENBACSI NVARCHAR(100),
    GIOITINH NVARCHAR(10),
    NAMSINH INT,
    KHOA_ID INT,
    SOBN INT,
    HOCHAM NVARCHAR(20),
    HOCVI NVARCHAR(50),
    CHUCVU NVARCHAR(50),
    AVATAR NVARCHAR(200),
    CONSTRAINT FK_BACSI_KHOA FOREIGN KEY (KHOA_ID) REFERENCES KHOA(KHOA_ID) ON DELETE CASCADE
);

-- 4. Table BENHNHAN
CREATE TABLE BENHNHAN (
    BNHAN_ID INT IDENTITY(1,1) PRIMARY KEY,
    MABENHNHAN AS ('BN' + RIGHT('000' + CAST(BNHAN_ID AS VARCHAR(4)), 3)) PERSISTED,
    HOTENBENHNHAN NVARCHAR(100),
    SDT VARCHAR(15),
    GIOITINH NVARCHAR(10),
    BACSI_ID INT,
    AVATAR NVARCHAR(200),
    CCCD VARCHAR(12),
    CONSTRAINT FK_BENHNHAN_BACSI FOREIGN KEY (BACSI_ID) REFERENCES BACSI(BACSI_ID)
);

-- 5. Table CAKHAM_MAPPING
CREATE TABLE CAKHAM_MAPPING (
    CAKHAM_ID INT PRIMARY KEY,
    GIOKHAM TIME(7)
);

-- 6. Table DATLICHKHAM
CREATE TABLE DATLICHKHAM (
    DATLICHKHAM_ID INT IDENTITY(1,1) PRIMARY KEY,
    BNHAN_ID INT,
    BACSI_ID INT,
    NGAYKHAM DATE,
    CAKHAM_ID INT,
    TRANGTHAI NVARCHAR(50),
    THOIGIANDAT DATETIME,
    CONSTRAINT FK_DATLICH_BENHNHAN FOREIGN KEY (BNHAN_ID) REFERENCES BENHNHAN(BNHAN_ID) ON DELETE CASCADE,
    CONSTRAINT FK_DATLICH_BACSI FOREIGN KEY (BACSI_ID) REFERENCES BACSI(BACSI_ID),
    CONSTRAINT FK_DATLICH_CAKHAM FOREIGN KEY (CAKHAM_ID) REFERENCES CAKHAM_MAPPING(CAKHAM_ID)
);

-- 7. Table DICHVU
CREATE TABLE DICHVU (
    DV_ID INT IDENTITY(1,1) PRIMARY KEY,
    MADV AS ('DV' + RIGHT('000' + CAST(DV_ID AS VARCHAR(4)), 3)) PERSISTED,
    TENDV NVARCHAR(100),
    DONGIA DECIMAL(18,2),
    GHICHU NVARCHAR(200),
    KHOA_ID INT,
    CONSTRAINT FK_DICHVU_KHOA FOREIGN KEY (KHOA_ID) REFERENCES KHOA(KHOA_ID) ON DELETE CASCADE
);

-- 8. Table HOADON
CREATE TABLE HOADON (
    HD_ID INT IDENTITY(1,1) PRIMARY KEY,
    MAHD AS ('HD' + RIGHT('000' + CAST(HD_ID AS VARCHAR(4)), 3)) PERSISTED,
    DATLICHKHAM_ID INT,
    NGAYLAP DATE,
    TONGTIEN DECIMAL(18,2),
    HINHTHUCTHANHTOAN NVARCHAR(50),
    GHICHU NVARCHAR(200),
    CONSTRAINT FK_HOADON_DATLICH FOREIGN KEY (DATLICHKHAM_ID) REFERENCES DATLICHKHAM(DATLICHKHAM_ID) ON DELETE CASCADE
);

-- 9. Table CHITIETHOADON
CREATE TABLE CHITIETHOADON (
    CTHD_ID INT IDENTITY(1,1) PRIMARY KEY,
    HOADON_ID INT,
    DICHVU_ID INT,
    SOLUONG INT,
    THANHTIEN DECIMAL(18,2),
    CONSTRAINT FK_CTHD_HOADON FOREIGN KEY (HOADON_ID) REFERENCES HOADON(HD_ID) ON DELETE CASCADE,
    CONSTRAINT FK_CTHD_DICHVU FOREIGN KEY (DICHVU_ID) REFERENCES DICHVU(DV_ID)
);

-- 10. Table KETQUAKHAM
CREATE TABLE KETQUAKHAM (
    KQ_ID INT IDENTITY(1,1) PRIMARY KEY,
    MAKQ AS ('KQ' + RIGHT('000' + CAST(KQ_ID AS VARCHAR(4)), 3)) PERSISTED,
    DATLICHKHAM_ID INT,
    CHUANDOAN NVARCHAR(300),
    KETLUAN NVARCHAR(300),
    THANHVIEN NVARCHAR(200),
    GHICHU NVARCHAR(200),
    CONSTRAINT FK_KQ_DATLICH FOREIGN KEY (DATLICHKHAM_ID) REFERENCES DATLICHKHAM(DATLICHKHAM_ID) ON DELETE CASCADE
);
GO

